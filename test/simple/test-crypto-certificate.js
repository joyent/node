var common = require('../common');
var assert = require('assert');

var crypto = require('crypto');
var certificate = new crypto.Certificate();

crypto.DEFAULT_ENCODING = 'buffer';

for (var key in spkac) {
  for (var k in spkac[key]) {

    var buf = new Buffer(spkac[key][k]);

    assert.equal(certificate.verifySpkac(buf), true);

    assert.equal(fixStr(certificate.exportChallenge(buf).toString('utf8')),
                 fixStr(val[key][k].challenge.toString('utf8')));

    assert.equal(fixStr(certificate.exportPublicKey(buf).toString('utf8')),
                 fixStr(val[key].publicKey.toString('utf8')));
  }
}

function fixStr(obj) {
  return obj.replace(/\n/g, '');
}
